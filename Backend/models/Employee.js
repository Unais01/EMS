const mongoose = require("mongoose");
const emloyeeSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      required: true,
    },
    organization: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
  },
  { timestamps: true }
);

const Employee = mongoose.model("Employee", emloyeeSchema);
module.exports = Employee;
